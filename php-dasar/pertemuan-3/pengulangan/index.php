<?php
// pengulangan PHP
// for
  for ($a = 0; $a < 5; $a++) {
    echo("Menggunakan for <br>");
  }
  
  echo("<hr>");
  
  // while
  $b = 0;
  while ($b < 5 ) {
    echo("Menggunakan while <br>");
    $b++;
  }
  
